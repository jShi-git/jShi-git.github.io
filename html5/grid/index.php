<script>

  $(function(){
    
    var $container = $('#gridholder');
    
    $container.isotope({
      itemSelector : '.polaroid',
      resizable: false,
      masonry: {
        columnWidth: $container.width() / 4
      }
    });
    
    $(window).smartresize(function(){
      $container.isotope({
        masonry: {
          columnWidth: $container.width() / 4
        }
      });
    });
    
    $container.delegate( '.polaroid > .closed', 'click', function(){
      $polars = $container.find(".polaroid");
      $polars.find(".open").removeClass('active');
      $polars.find(".closed").addClass("active");

      $(this).toggleClass('active');
      open = $(this).next(".open");
      open.hasClass("active") ? open.removeClass("active") : open.addClass('active');
      $container.isotope('reLayout');
    });

    $container.delegate( '.polaroid > .open > .close', 'click', function(){
      $(this).parent().removeClass('active');
      $(this).parent().prevAll(".closed").addClass('active');
      $container.isotope('reLayout');
    });
  });
</script>
